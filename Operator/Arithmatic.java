import java.util.*;
public class Arithmatic {
    public static void main(String[] args) {
        int a = 8;
        int b = 5;
      
      //Arithmatic operator
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b); //modulo operator only work on integer value
    } 
}